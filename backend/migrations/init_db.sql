-- =====================
-- INSERT NEIGHBORHOOD GROUPS (10 per Ward = 500 total)
-- Using a DO block to generate programmatically
-- =====================

DO $$
DECLARE
    ward_record RECORD;
    i INTEGER;
BEGIN
    FOR ward_record IN SELECT id, code FROM wards LOOP
        FOR i IN 1..10 LOOP
            INSERT INTO neighborhood_groups (ward_id, name, code)
            VALUES (
                ward_record.id,
                'Tổ ' || i,
                ward_record.code || '-T' || i
            )
            ON CONFLICT DO NOTHING;
        END LOOP;
    END LOOP;
END $$;

